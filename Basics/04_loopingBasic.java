package Basics;

public class loopingBasic {
    public static void main(String[] args) {
        //for Loop
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }

        //while Loop
        int i = 1;
        while (i <= 5) {
            System.out.println(i);
            i++;
        }

        //do-while Loop
        
        int s = 1;
        do {
            System.out.println(s);
            s++;
        } while (s <= 5);

        
    
    }


}
